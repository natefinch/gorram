package testfuncs

// DoubleUint64 uses a uint64 as an argument for testing purposes. It returns 2x
// the argument.
func DoubleUint64(a uint64) uint64 {
	return a * 2
}

// DoubleUint uses a uint as an argument for testing purposes. It returns 2x
// the argument.
func DoubleUint(a uint) uint {
	return a * 2
}
